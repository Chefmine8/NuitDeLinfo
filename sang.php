    <h1>pH et sang</h1>
    <p>Le pH de l’eau océanique est essentiel à la survie des espèces marines. Une acidification excessive (causée par l’absorption de CO₂) perturbe la santé des écosystèmes, mettant en danger les coraux, les mollusques et bien d'autres. Ainsi, l’augmentation du pH de l’océan est, tout comme celle du sang chez un humain, extrêmement dangereuse.</p>
