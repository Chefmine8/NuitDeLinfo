    <h1>Système digestif - Phytoplanctons</h1>
    <p>Les phytoplanctons sont le système digestif de l’océan. En captant l’énergie du soleil grâce à la photosynthèse, ils alimentent toute la chaîne alimentaire marine, des plus petites crevettes aux plus grands prédateurs comme les baleines. Tout comme notre corps a besoin d’être nourri pour survivre, l’océan a besoin des phytoplanctons pour que la chaîne alimentaire ne soit pas détruite. Cependant le phytoplancton est confronté à des défis multiples, comme le réchauffement climatique, l’acidification des océans, diminution des nutriments, pollution, rayonnement UV accru et perturbations climatiques. Ces menaces amènent des conséquences graves pour l'équilibre écologique des océans.</p>
