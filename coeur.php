    <h1>Coeur - Récifs coralients</h1>
    <p>Les récifs coralliens sont le "cœur battant" des océans. Ils abritent une biodiversité incroyable, fournissent un habitat pour des milliers d'espèces et protègent les côtes des vagues et des tempêtes. Sans les coraux, de nombreuses espèces marines ne pourraient pas survivre. Tout comme dans le corps humain, ils sont le centre de la vie des océans. Sans ce cœur, l’océan ne pourrait pas survivre. Aujourd’hui, on remarque sur toute la planète un blanchissement des coraux et une disparition de certaines parties de la barrière de corail. Il est donc essentiel de le protéger en limitant notre impact CO2 ainsi que la pollution des mers et océans.</p>
