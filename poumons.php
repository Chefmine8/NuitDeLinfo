    <h1>Poumons - Algues & Phytoplanctons</h1>
    <p>Tout comme les poumons de notre cœur nous apportent de l’oxygène et nous permettent de respirer, le phytoplancton et les algues présents dans l’océan permettent à la terre de respirer, en absorbant le CO2 et en produisant plus de 50 % de l’oxygène que nous respirons. Cependant le phytoplancton et les algues absorbent de moins en moins de CO2 notamment à cause du réchauffement climatique, l’acidification des Océan, la pollution, la perturbation des cycles des nutriments et les effets des UV. </p>
