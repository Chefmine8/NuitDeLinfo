    <h1>Peau et surface de l'océan</h1>
    <p>Comme la peau humaine protège le corps des agressions extérieures, la surface de l’océan joue un rôle protecteur pour l'écosystème marin. Elle aide aussi à réguler la chaleur, un peu comme la transpiration régule notre température corporelle. Une peau abîmée expose le corps aux infections et déséquilibres. De la même manière, une surface océanique perturbée, par exemple par la pollution ou le réchauffement, menace tout l'équilibre des écosystèmes marins.</p>
